#include "lemin.h"

int		ft_comment(char *line)
{
	int		i;

	i = ft_strlen(line);
	if (i == 1 && line[0] == '#')
		return (1);
	if (i > 1 && line[0] == '#' && line[1] != '#')
		return (1);
	return (0);
}
